    // Расчет материалов для заказа
    calculateMaterials(order) {
        const materials = [];
        
        order.items.forEach(item => {
            const quantity = item.quantity || 1;
            const productName = item.product;
            const size = item.size;
            
            console.log(`Расчет материалов для ${productName} размер ${size}, кол-во ${quantity}`);
            
            // 1. Сначала добавляем кронштейн и лиру из заказа
            if (item.bracket && item.bracket !== 'отсутствует') {
                const bracket = this.materialsDB.brackets.find(b => b.name === item.bracket);
                if (bracket) {
                    materials.push({
                        name: `Кронштейн ${item.bracket}`,
                        material: 'AISI 430',
                        unitWeight: bracket.weight || 0,
                        quantity: quantity,
                        totalWeight: (bracket.weight || 0) * quantity,
                        source: 'Заказ'
                    });
                }
            }
            
            if (item.lyre && item.lyre !== 'отсутствует') {
                const lyre = this.materialsDB.lyres.find(l => l.name === item.lyre);
                if (lyre) {
                    materials.push({
                        name: `Лира ${item.lyre}`,
                        material: 'AISI 430',
                        unitWeight: lyre.weight || 0,
                        quantity: quantity,
                        totalWeight: (lyre.weight || 0) * quantity,
                        source: 'Заказ'
                    });
                }
            }
            
            // 2. Добавляем материалы из техкарты изделия
            const productSpec = this.materialsDB.productSpecs[productName];
            if (productSpec && productSpec[size]) {
                const spec = productSpec[size];
                
                // Добавляем все колонки из техкарты
                Object.entries(spec).forEach(([key, value]) => {
                    // Пропускаем "Размер светильника" так как это не материал
                    if (key.includes('Размер')) return;
                    
                    // Определяем тип материала по названию колонки
                    let materialType = 'Комплектующие';
                    if (key.includes('Профиль')) materialType = 'Профиль';
                    else if (key.includes('Модуль')) materialType = 'Модуль';
                    else if (key.includes('Планка')) materialType = 'Планка';
                    else if (key.includes('КП')) materialType = 'Комплект';
                    
                    materials.push({
                        name: `${key} (${productName})`,
                        material: materialType,
                        unitValue: value,
                        unit: 'мм',
                        quantity: quantity,
                        totalValue: value * quantity,
                        source: 'Техкарта изделия'
                    });
                });
            }
        });
        
        return this.aggregateMaterials(materials);
    }
    
    // Агрегация материалов
    aggregateMaterials(materials) {
        const aggregated = {};
        
        materials.forEach(m => {
            const key = `${m.material}_${m.name}`;
            if (!aggregated[key]) {
                aggregated[key] = {
                    material: m.material,
                    name: m.name,
                    totalWeight: 0,
                    totalValue: 0,
                    unit: m.unit || 'кг',
                    quantity: 0,
                    items: []
                };
            }
            
            if (m.totalWeight) {
                aggregated[key].totalWeight += m.totalWeight;
            }
            if (m.totalValue) {
                aggregated[key].totalValue += m.totalValue;
            }
            aggregated[key].quantity += m.quantity;
            aggregated[key].items.push(m);
        });
        
        return Object.values(aggregated);
    }
    
